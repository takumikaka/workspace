public class SwitchTestTwo{
    public static void main(String[] args){
        char grade = 'C';
        switch(grade){
            case 'A':
                System.out.println("you");
                break;
            case 'B':
                System.out.println("liang");
                break;
            case 'C':
                System.out.println("jige");
                break;
            case 'F':
                System.out.println("cha");
                break;
            default:
                System.out.println("cheng ji shu ru cuo wu");
        }
    }
}
