public class DoubleIfElse{
    public static void main(String[] args){
        int x = 30;
        if (x == 10){
            System.out.print("x is 10...\n");
        }else if(x == 20){
            System.out.print("x is 20...\n");
        }else if(x == 30){
            System.out.print("x is 30...\n");
        }else{
            System.out.print("No x value...\n");
        }
    }
}
