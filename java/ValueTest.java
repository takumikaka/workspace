public class ValueTest{
    public static void main(String[] args){
        int a = 10;
        int b = 10;
        System.out.println("a--:" + (a--)); // 10
        System.out.println("a++:" + (a++)); // 9
        System.out.println("++b:" + (++b)); // 11
        System.out.println("--b:" + (--b)); // 10
    }
}
